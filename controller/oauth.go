// providerParams returns map with Provider key for i18n templates
func providerParams(name string) map[string]any {
	return map[string]any{"Provider": name}
}

const (
	oauthStateKey      = "oauth_state"
	oauthBindIntentKey = "oauth_bind_intent"
)

// isValidSignedOAuthState validates the stateless HMAC form generated by GenerateOAuthCode.
// It is used as a fallback when the browser does not send the OAuth session cookie
// back to the API callback host (for example ai.* frontend -> api.* callback).
func isValidSignedOAuthState(state string) bool {
	parts := strings.Split(state, ".")
	if len(parts) != 2 || parts[0] == "" || parts[1] == "" {
		return false
	}
	expected := common.HmacSha256("oauth-state:"+parts[0], common.SessionSecret)
	return hmac.Equal([]byte(parts[1]), []byte(expected))
}

// GenerateOAuthCode generates a state code for OAuth CSRF protection
func GenerateOAuthCode(c *gin.Context) {
	session := sessions.Default(c)
	session.Delete(oauthBindIntentKey)
	stateNonce := common.GetRandomString(24)
	state := stateNonce + "." + common.HmacSha256("oauth-state:"+stateNonce, common.SessionSecret)
	affCode := c.Query("aff")
	if affCode != "" {
		session.Set("aff", affCode)
	}
	session.Set(oauthStateKey, state)
	err := session.Save()
	if err != nil {
		common.ApiError(c, err)
		return
	}
	c.JSON(http.StatusOK, gin.H{
		"success": true,
		"message": "",
		"data":    state,
	})
}

// GenerateOAuthBindCode generates a state code for an authenticated OAuth binding flow.
// Normal OAuth login/register must use GenerateOAuthCode; binding additionally requires
// this session intent so cached or stale frontend callbacks cannot be mistaken for binding.
func GenerateOAuthBindCode(c *gin.Context) {
	providerName := c.Param("provider")
	provider := oauth.GetProvider(providerName)
	if provider == nil {
		c.JSON(http.StatusBadRequest, gin.H{
			"success": false,
			"message": i18n.T(c, i18n.MsgOAuthUnknownProvider),
		})
		return
	}
	if !provider.IsEnabled() {
		common.ApiErrorI18n(c, i18n.MsgOAuthNotEnabled, providerParams(provider.GetName()))
		return
	}

	session := sessions.Default(c)
	stateNonce := common.GetRandomString(24)
	state := stateNonce + "." + common.HmacSha256("oauth-state:"+stateNonce, common.SessionSecret)
	session.Set(oauthStateKey, state)
	session.Set(oauthBindIntentKey, providerName)
	if err := session.Save(); err != nil {
		common.ApiError(c, err)
		return
	}
	c.JSON(http.StatusOK, gin.H{
		"success": true,
		"message": "",
		"data":    state,
	})
}

// HandleOAuth handles OAuth callback for all standard OAuth providers
func HandleOAuth(c *gin.Context) {
	providerName := c.Param("provider")
	provider := oauth.GetProvider(providerName)
	if provider == nil {
		c.JSON(http.StatusBadRequest, gin.H{
			"success": false,
			"message": i18n.T(c, i18n.MsgOAuthUnknownProvider),
		})
		return
	}

	session := sessions.Default(c)

	// 1. Validate state (CSRF protection)
	state := c.Query("state")
	sessionState, _ := session.Get(oauthStateKey).(string)
	if state == "" || (state != sessionState && !isValidSignedOAuthState(state)) {
		c.JSON(http.StatusForbidden, gin.H{
			"success": false,
			"message": i18n.T(c, i18n.MsgOAuthStateInvalid),
		})
		return
	}

	// 2. Check if this is an explicit bind flow.
	// Binding requires an authenticated session plus a server-side bind intent created
	// by /api/oauth/:provider/bind/state. Do not infer binding from an existing login
	// session or from a bare bind=true query: normal OAuth login/register may keep an
	// old session cookie, and stale frontend cache can otherwise show "绑定成功".
	isBindRequest := c.Query("bind") == "true" || c.Query("bind") == "1"
	if isBindRequest {
		bindIntent, _ := session.Get(oauthBindIntentKey).(string)
		username := session.Get("username")
		id := session.Get("id")
		if bindIntent == providerName && username != nil && id != nil && state == sessionState {
			session.Delete(oauthBindIntentKey)
			_ = session.Save()
			handleOAuthBind(c, provider)
			return
		}
		common.SysLog(fmt.Sprintf("[OAuth] ignored bind callback without valid bind intent provider=%s intent=%s state_match=%t username_present=%t id_present=%t", providerName, bindIntent, state == sessionState, username != nil, id != nil))
	}
	// Any non-binding callback is login/register. Clear stale bind intent before the
	// login path so registration/login can never be converted into an account bind.
	session.Delete(oauthBindIntentKey)
	_ = session.Save()

	// 3. Check if provider is enabled
	if !provider.IsEnabled() {
		common.ApiErrorI18n(c, i18n.MsgOAuthNotEnabled, providerParams(provider.GetName()))
		return
	}

	// 4. Handle error from provider
	errorCode := c.Query("error")
	if errorCode != "" {
		errorDescription := c.Query("error_description")
		c.JSON(http.StatusOK, gin.H{
			"success": false,
			"message": errorDescription,
		})
		return
	}

	// 5. Exchange code for token
	code := c.Query("code")
	token, err := provider.ExchangeToken(c.Request.Context(), code, c)
	if err != nil {
		handleOAuthError(c, err)
		return
	}

	// 6. Get user info
	oauthUser, err := provider.GetUserInfo(c.Request.Context(), token)
	if err != nil {
		handleOAuthError(c, err)
		return
	}

	// 7. Find or create user
	user, err := findOrCreateOAuthUser(c, provider, oauthUser, session)
	if err != nil {
		switch err.(type) {
		case *OAuthUserDeletedError:
			common.ApiErrorI18n(c, i18n.MsgOAuthUserDeleted)
		case *OAuthRegistrationDisabledError:
			common.ApiErrorI18n(c, i18n.MsgUserRegisterDisabled)
		default:
			common.ApiError(c, err)
		}
		return
	}

	// 8. Check user status
	if user.Status != common.UserStatusEnabled {
		common.ApiErrorI18n(c, i18n.MsgOAuthUserBanned)
		return
	}

	tryCommunityBotAutomation(c.Request.Context(), provider, oauthUser, user)

	// 9. Setup login
	setupLogin(user, c)
}

// handleOAuthBind handles binding OAuth account to existing user
func handleOAuthBind(c *gin.Context, provider oauth.Provider) {
	if !provider.IsEnabled() {
		common.ApiErrorI18n(c, i18n.MsgOAuthNotEnabled, providerParams(provider.GetName()))
		return
	}

	// Exchange code for token
	code := c.Query("code")
	token, err := provider.ExchangeToken(c.Request.Context(), code, c)
	if err != nil {
		handleOAuthError(c, err)
		return
	}

	// Get user info
	oauthUser, err := provider.GetUserInfo(c.Request.Context(), token)
	if err != nil {
		handleOAuthError(c, err)
		return
	}

	// Check if this OAuth account is already bound (check both new ID and legacy ID)
	if provider.IsUserIDTaken(oauthUser.ProviderUserID) {
		common.ApiErrorI18n(c, i18n.MsgOAuthAlreadyBound, providerParams(provider.GetName()))
		return
	}
	// Also check legacy ID to prevent duplicate bindings during migration period
	if legacyID, ok := oauthUser.Extra["legacy_id"].(string); ok && legacyID != "" {
		if provider.IsUserIDTaken(legacyID) {
			common.ApiErrorI18n(c, i18n.MsgOAuthAlreadyBound, providerParams(provider.GetName()))
			return
		}
	}

	// Get current user from session
	session := sessions.Default(c)
	id, ok := session.Get("id").(int)
	if !ok || id <= 0 {
		common.ApiErrorI18n(c, i18n.MsgOAuthConnectFailed, providerParams(provider.GetName()))
		return
	}
	user := model.User{Id: id}
	err = user.FillUserById()
	if err != nil {
		common.ApiError(c, err)
		return
	}

	// Handle binding based on provider type
	if genericProvider, ok := provider.(*oauth.GenericOAuthProvider); ok {
		// Custom provider: use user_oauth_bindings table
		err = model.UpdateUserOAuthBinding(user.Id, genericProvider.GetProviderId(), oauthUser.ProviderUserID)
		if err != nil {
			common.ApiError(c, err)
			return
		}
	} else {
		// Built-in provider: update user record directly
		provider.SetProviderUserID(&user, oauthUser.ProviderUserID)
		err = user.Update(false)
		if err != nil {
			common.ApiError(c, err)
			return
		}
	}

	common.ApiSuccessI18n(c, i18n.MsgOAuthBindSuccess, gin.H{
		"action": "bind",
	})
}

// findOrCreateOAuthUser finds existing user or creates new user
func findOrCreateOAuthUser(c *gin.Context, provider oauth.Provider, oauthUser *oauth.OAuthUser, session sessions.Session) (*model.User, error) {
	user := &model.User{}

	// Check if user already exists with new ID
	if provider.IsUserIDTaken(oauthUser.ProviderUserID) {
		err := provider.FillUserByProviderID(user, oauthUser.ProviderUserID)
		if err != nil {
			return nil, err
		}
		// Check if user has been deleted
		if user.Id == 0 {
			return nil, &OAuthUserDeletedError{}
		}
		return user, nil
	}

	// Try to find user with legacy ID (for GitHub migration from login to numeric ID)
	if legacyID, ok := oauthUser.Extra["legacy_id"].(string); ok && legacyID != "" {
		if provider.IsUserIDTaken(legacyID) {
			err := provider.FillUserByProviderID(user, legacyID)
			if err != nil {
				return nil, err
			}
			if user.Id != 0 {
				// Found user with legacy ID, migrate to new ID
				common.SysLog(fmt.Sprintf("[OAuth] Migrating user %d from legacy_id=%s to new_id=%s",
					user.Id, legacyID, oauthUser.ProviderUserID))
				if err := user.UpdateGitHubId(oauthUser.ProviderUserID); err != nil {
					common.SysError(fmt.Sprintf("[OAuth] Failed to migrate user %d: %s", user.Id, err.Error()))
					// Continue with login even if migration fails
				}
				return user, nil
			}
		}
	}

	// 防重门：在创建新账号前，再用 username 兜底查一次该 provider 下的已有绑定。
	// 历史上 ProviderUserID 标识不稳定（曾因字段映射问题取到 username 或空值），
	// 仅靠上面的 IsUserIDTaken(ProviderUserID) 可能漏判已存在用户，从而重复建号。
	// 这里对自定义 provider 用 username 再查一遍绑定，任一命中即复用，杜绝重复账号。
	if genericProvider, ok := provider.(*oauth.GenericOAuthProvider); ok {
		for _, ident := range []string{
			strings.TrimSpace(oauthUser.ProviderUserID),
			strings.TrimSpace(oauthUser.Username),
		} {
			if ident == "" {
				continue
			}
			if existing, err := model.GetUserByOAuthBinding(genericProvider.GetProviderId(), ident); err == nil && existing != nil && existing.Id > 0 {
				common.SysLog(fmt.Sprintf("[OAuth] reuse existing bound user id=%d via ident=%s provider=%s (prevent duplicate account)",
					existing.Id, ident, genericProvider.GetConfig().Slug))
				*user = *existing
				return user, nil
			}
		}
	}

	// User doesn't exist, create new user if registration is enabled
	if !common.RegisterEnabled {
		return nil, &OAuthRegistrationDisabledError{}
	}
	if !communityGateAllowsOAuthRegistration(provider) {
		return nil, &OAuthRegistrationDisabledError{}
	}

	// Set up new user
	user.Username = provider.GetProviderPrefix() + strconv.Itoa(model.GetMaxUserId()+1)

	if oauthUser.Username != "" {
		if exists, err := model.CheckUserExistOrDeleted(oauthUser.Username, ""); err == nil && !exists {
			// 防止索引退化
			if len(oauthUser.Username) <= model.UserNameMaxLength {
				user.Username = oauthUser.Username
			}
		}
	}

	if oauthUser.DisplayName != "" {
		user.DisplayName = oauthUser.DisplayName
	} else if oauthUser.Username != "" {
		user.DisplayName = oauthUser.Username
	} else {
		user.DisplayName = provider.GetName() + " User"
	}
	if oauthUser.Email != "" {
		user.Email = oauthUser.Email
	}
	user.Role = common.RoleCommonUser
	user.Status = common.UserStatusEnabled

	// Handle affiliate code
	affCode := session.Get("aff")
	inviterId := 0
	if affCode != nil {
		inviterId, _ = model.GetUserIdByAffCode(affCode.(string))
	}

	// Use transaction to ensure user creation and OAuth binding are atomic
	if genericProvider, ok := provider.(*oauth.GenericOAuthProvider); ok {
		// Custom provider: create user and binding in a transaction
		err := model.DB.Transaction(func(tx *gorm.DB) error {
			// Create user
			if err := user.InsertWithTx(tx, inviterId); err != nil {
				return err
			}

			// Create OAuth binding
			binding := &model.UserOAuthBinding{
				UserId:         user.Id,
				ProviderId:     genericProvider.GetProviderId(),
				ProviderUserId: oauthUser.ProviderUserID,
			}
			if err := model.CreateUserOAuthBindingWithTx(tx, binding); err != nil {
				return err
			}

			return nil
		})
		if err != nil {
			return nil, err
		}

		// Perform post-transaction tasks (logs, sidebar config, inviter rewards)
		user.FinalizeOAuthUserCreation(inviterId)
	} else {
		// Built-in provider: create user and update provider ID in a transaction
		err := model.DB.Transaction(func(tx *gorm.DB) error {
			// Create user
			if err := user.InsertWithTx(tx, inviterId); err != nil {
				return err
			}

			// Set the provider user ID on the user model and update
			provider.SetProviderUserID(user, oauthUser.ProviderUserID)
			if err := tx.Model(user).Updates(map[string]interface{}{
				"github_id":   user.GitHubId,
				"discord_id":  user.DiscordId,
				"oidc_id":     user.OidcId,
				"linux_do_id": user.LinuxDOId,
				"wechat_id":   user.WeChatId,
				"telegram_id": user.TelegramId,
			}).Error; err != nil {
				return err
			}

			return nil
		})
		if err != nil {
			return nil, err
		}

		// Perform post-transaction tasks
		user.FinalizeOAuthUserCreation(inviterId)
	}

	return user, nil
}

func communityGateAllowsOAuthRegistration(provider oauth.Provider) bool {
	cfg := operation_setting.GetCommunityGateSetting()
	if cfg == nil || !cfg.Enabled {
		return true
	}
	genericProvider, isGeneric := provider.(*oauth.GenericOAuthProvider)
	if cfg.OnlyAllowProviderRegister {
		if !isGeneric || genericProvider.GetConfig() == nil {
			return false
		}
		return strings.TrimSpace(genericProvider.GetConfig().Slug) == strings.TrimSpace(cfg.ProviderSlug)
	}
	if cfg.DisableBuiltinOAuthRegister && !isGeneric {
		return false
	}
	return true
}

func tryCommunityBotAutomation(ctx context.Context, provider oauth.Provider, oauthUser *oauth.OAuthUser, user *model.User) {
	genericProvider, ok := provider.(*oauth.GenericOAuthProvider)
	if !ok || oauthUser == nil || user == nil {
		return
	}
	config := genericProvider.GetConfig()
	if config == nil {
		return
	}
	service.InviteCommunityUserIfEnabled(ctx, genericProvider.GetProviderId(), config.Slug, oauthUser.ProviderUserID, oauthUser.Username)
	service.GrantCommunityJoinRewardIfNeeded(ctx, genericProvider.GetProviderId(), config.Slug, oauthUser.ProviderUserID, oauthUser.Username, user)
}

func truncateForLog(s string, max int) string {
	if len(s) <= max {
		return s
	}
	return s[:max] + "..."
}

// Error types for OAuth
type OAuthUserDeletedError struct{}

func (e *OAuthUserDeletedError) Error() string {
	return "user has been deleted"
}

type OAuthRegistrationDisabledError struct{}

func (e *OAuthRegistrationDisabledError) Error() string {
	return "registration is disabled"
}

// handleOAuthError handles OAuth errors and returns translated message
func handleOAuthError(c *gin.Context, err error) {
	switch e := err.(type) {
	case *oauth.OAuthError:
		if e.Params != nil {
			common.ApiErrorI18n(c, e.MsgKey, e.Params)
		} else {
			common.ApiErrorI18n(c, e.MsgKey)
		}
	case *oauth.AccessDeniedError:
		common.ApiErrorMsg(c, e.Message)
	case *oauth.TrustLevelError:
		common.ApiErrorI18n(c, i18n.MsgOAuthTrustLevelLow)
	default:
		common.ApiError(c, err)
	}
}
